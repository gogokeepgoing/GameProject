package FlashGameProject;

public class HangManDTO {
	
	private String word;
	private int num;
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	@Override
	public String toString() {
		return "HangManDTO [word=" + word + ", num=" + num + "]";
	}


	

	
	

}
